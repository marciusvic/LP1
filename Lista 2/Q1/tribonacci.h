#ifndef TRIBONACCI_H
#define TRIBONACCI_H

/**
 * Calcula o n-ésimo termo da sequência de Tribonacci utilizando um algoritmo iterativo.
 * @param n o índice do termo desejado.
 * @return o n-ésimo termo da sequência de Tribonacci.
 */
int tribonacci_iterativa(int n);

/**
 * Calcula o n-ésimo termo da sequência de Tribonacci utilizando um algoritmo recursivo.
 * @param n o índice do termo desejado.
 * @return o n-ésimo termo da sequência de Tribonacci.
 */
int tribonacci_recursiva(int n);

#endif /* TRIBONACCI_H */
